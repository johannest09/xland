						<h2>Hvað er XLAND? </h2>
						<p>XLAND er lifandi gátt sem býður þér, fjölskyldu þinni og vinum í ferðalag um vel hönnuð borgarrými. Félag íslenskra landslagsarkitekta stendur fyrir fyrstu gagnvirku sýningunni þar sem verkum landslagsarkitekta á höfuðborgarsvæðinu er komið á kortið í bókstaflegum skilningi. Verkefnið er unnið í tengslum við Hönnunarmars 2013 og hlaut nafnið XLAND, sem vísar til meginmarkmiðs síðunnar að merkja með X-i og vekja athygli á vel hönnuðum og áhugaverðum almenningsrýmum í borginni okkar. Við stefnum að sjálfsögðu á að XLAND nái yfir Ísland allt þegar fram líða stundir.</p> 
						<p>Verkin eru staðsett á gagnvirku korti af höfuðborgarsvæðinu. Sé smellt á verk opnast gátt þar sem verkunum er miðlað með ljósmyndum, teikningum grafískum skissum, myndböndum og texta. Þannig kemst þú í návígi við hönnuðina og hugsunina sem liggur að baki borgarrýmum í þínu nánasta umhverfi.</p>
						<p>Í tengslum við opnun heimasíðunnar er gagnvirk sýning í Hafnarhúsinu, Listasafni Reykjavíkur dagana 14. - 17. mars þar sem gestum gefst tækifæri til að vafra um vel hönnuð og lifandi borgarrými á risavöxnum skjá. Heimasíðan er jafnframt kærkomin viðbót í afþreyingarflóru höfuðborgarinnar þar sem hægt er að fletta upp almenningsrýmum hvar sem þú ert á ferðinni í gegnum tölvu, i-pad, síma o.s.frv. </p>
